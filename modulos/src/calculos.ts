export class Soma {
    public calcular(numero1: number, numero2: number): number {
        return numero1 + numero2
    }
} 

export class Subtracao {
    public calcular(numero1: number, numero2: number): number {
        return numero1 - numero2
    }
}

export class Divisao {
    public calcular(numero1: number, numero2: number): number {
        return numero1 / numero2
    }
}

export class Multiplicacao {
    public calcular(numero1: number, numero2: number): number {
        return numero1 * numero2
    }
}